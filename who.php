    <h3>Blood Safety</h3>
    <p> We must make sure it is safe for people to give blood and for patients to receive donated blood. Blood safety information is below: </p><p>
    You must not give blood or platelets if...</p>
    <ul>
        <li>You think you need a test for HIV/AIDS, HTLV or hepatitis.</li>
        <li>You have ever had or been treated for syphilis</li>
        <li>You are HTLV positive</li>
        <li>You have ever injected, or been injected with, drugs; even a long time ago or only once. This includes body-building drugs and injectable tanning agents. You may be able to give if a doctor prescribed the drugs. Please check with us to make sure.</li>
        <li>You are working as a sex worker. You may be accepted for donation if it is longer than 3 months since you last received money or drugs for sex</li>
        
    </ul>
