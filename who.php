    <h2>Most people can give blood. You can give blood if you:</h2>
    <ul>
        <li>are fit and healthy </li>
        <li> weigh between 7 stone 12 lbs and 25 stone, or 50kg and 160kg</li>
        <li> are aged between 17 and 66 (or 70 if you have given blood before)</li>
        <li> are over 70 and have given blood in the last two years</li>
    </ul>
    <h5>How Often can i give Blood</h5>
    <p>
        Men can give blood every 12 weeks and women can give blood every 16 weeks. Find out more about what happens on the day of your donation.
        </p>
    <h5>Check you are able to give blood</h5>
    <p>You can check some of the most common eligibility questions we receive from blood donors.</p>
    <p>
    If you have a health condition, have travelled out of the country recently, or if you answer "yes" to any question on your Donor Health Check questionnaire, please call 0300 123 23 23 or check the health & eligibility or travel section for further advice about whether this affects you donating blood.</p><p>
    You can register here as a blood donor.</p>
    <p>
The common reasons donors should check if they can give blood are:</p>
    <ul>
        <li>if you are receiving medical or hospital treatment</li>
        <li>if you are taking medication</li>
        <li>after travelling outside of the UK</li>
        <li>after having a tattoo or piercing</li>
        <li>during and after pregnancy</li>
        <li>if you feel ill</li>
        <li>if you have cancer</li>
        <li>after receiving blood, blood products or organs.</li>
        
    </ul>
    <h5>Women under 20 - check if you can give blood</h5>
    <p>If you are a woman under 20 and you weigh under 10st 3lb or 65kg or are under 5' 6" or 168cm tall you will need to estimate your blood volume to see if you can give blood. If your weight lies between two of the values shown, please use the nearest lower weight.</p><p><u>Female Blood Donors, aged 17-20</u> </p><p>
 
Please find your height and weight and check which box these lines meet.</p>
 
<p>If your estimated blood volume is within the shaded area (i.e. less than 3500ml), unfortunately you are unable to donate until after your 20th birthday. This measure is to minimise the risk of fainting, in accordance with our research. </p> 
